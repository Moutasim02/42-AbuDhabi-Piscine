#include <unistd.h>

void	ft_is_negative(int n)
{
	char	c;
	char	p;

	c = 78;
	p = 80;
	if (n < 0)
	{
		write(1, &c, 1);
	}
	else if (n >= 0)
	{
		write(1, &p, 1);
	}
}
