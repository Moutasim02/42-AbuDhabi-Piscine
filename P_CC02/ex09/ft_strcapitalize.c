#include<unistd.h>

char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 65 && str[i] <= 90)
		{
			str[i] = str[i] + 32;
		}
		i++;
	}
	return (str);
}

char	*ft_strcapitalize(char *str)
{
	int	i;
	int	temp;

	i = 0;
	temp = 1;
	ft_strlowcase(str);
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			if (temp == 1)
				str[i] -= 32;
			temp = 0;
		}
		else if (str[i] >= '0' && str[i] <= '9')
			temp = 0;
		else
			temp = 1;
		i++;
	}
	return (str);
}
